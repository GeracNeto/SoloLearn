const isVacation = (month: string) => {

    switch (month) {
        case 'January':
            return '';
        case 'February':
            return '';
        case 'March':
            return '';
        case 'April':
            return '';
        case 'May':
            return '';
        case 'June':
            return '';
        case 'July':
            return '';
        case 'August':
            return 'vacation';
        case 'September':
            return '';
        case 'October':
            return '';
        case 'November':
            return '';
        case 'December':
            return ''
        default:
            return '';
    }
}

